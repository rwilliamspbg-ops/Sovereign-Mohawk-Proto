#!/usr/bin/env python3
"""Generate CPU vs GPU vs NPU benchmark comparison artifacts.

The benchmark is intentionally lightweight so it can run in CI and on laptops.
It compares quantization and reduction workloads under three backend policies.
"""

from __future__ import annotations

import argparse
import importlib
import json
import math
import os
import statistics
import sys
import time
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT / "sdk" / "python"))

accelerator = importlib.import_module("mohawk.accelerator")
Backend = accelerator.Backend
DeviceInfo = accelerator.DeviceInfo
build_auto_tune_profile = accelerator.build_auto_tune_profile
fp32_to_fp16 = accelerator.fp32_to_fp16
quantize_int8 = accelerator.quantize_int8


def make_vectors(clients: int, dim: int) -> list[list[float]]:
    vectors: list[list[float]] = []
    for c in range(clients):
        row = [((c + i) % 97) * 0.01 for i in range(dim)]
        vectors.append(row)
    return vectors


def aggregate_mean(vectors: list[list[float]]) -> list[float]:
    if not vectors:
        return []
    dim = len(vectors[0])
    accum = [0.0] * dim
    for row in vectors:
        for i in range(dim):
            accum[i] += row[i]
    inv = 1.0 / float(len(vectors))
    return [x * inv for x in accum]


def timed_ms(fn, repeats: int = 3) -> float:
    samples: list[float] = []
    for _ in range(repeats):
        start = time.perf_counter()
        fn()
        samples.append((time.perf_counter() - start) * 1000.0)
    return round(statistics.mean(samples), 3)


def run_backend_case(name: str, backend: str, npu_available: bool) -> dict[str, object]:
    vectors = make_vectors(clients=256, dim=4096)
    flat = vectors[0]

    synthetic_devices = [
        DeviceInfo(backend=Backend.CPU, name="CPU", simd_width=256),
        DeviceInfo(backend=Backend.CUDA, name="CUDA GPU", simd_width=128, memory_mb=16384),
        DeviceInfo(backend=Backend.NPU, name="NPU", simd_width=128),
    ]

    old_backend = os.environ.get("MOHAWK_ACCELERATOR_BACKEND")
    old_npu = os.environ.get("MOHAWK_NPU_AVAILABLE")
    try:
        os.environ["MOHAWK_ACCELERATOR_BACKEND"] = backend
        os.environ["MOHAWK_NPU_AVAILABLE"] = "true" if npu_available else "false"

        profile = build_auto_tune_profile(vector_length=4096, devices=synthetic_devices)

        fp16_ms = timed_ms(lambda: fp32_to_fp16(flat))
        int8_ms = timed_ms(lambda: quantize_int8(flat))
        agg_ms = timed_ms(lambda: aggregate_mean(vectors))

        return {
            "scenario": name,
            "selected_backend": profile.selected_device.backend,
            "preferred_format": profile.preferred_format,
            "recommended_workers": profile.recommended_workers,
            "fp16_quantize_ms": fp16_ms,
            "int8_quantize_ms": int8_ms,
            "aggregate_mean_ms": agg_ms,
            "notes": "Policy-driven backend profile; execution performed on current host runtime.",
        }
    finally:
        if old_backend is None:
            os.environ.pop("MOHAWK_ACCELERATOR_BACKEND", None)
        else:
            os.environ["MOHAWK_ACCELERATOR_BACKEND"] = old_backend

        if old_npu is None:
            os.environ.pop("MOHAWK_NPU_AVAILABLE", None)
        else:
            os.environ["MOHAWK_NPU_AVAILABLE"] = old_npu


def write_markdown(path: Path, rows: list[dict[str, object]]) -> None:
    lines = [
        "# Accelerator Backend Comparison (CPU vs GPU vs NPU)",
        "",
        "This report is generated by scripts/benchmark_accelerator_backends.py and is intended for CI + release evidence.",
        "",
        "| Scenario | Selected Backend | Preferred Format | Recommended Workers | FP16 Quantize (ms) | INT8 Quantize (ms) | Aggregate Mean (ms) |",
        "|---|---|---|---:|---:|---:|---:|",
    ]

    for row in rows:
        lines.append(
            "| {scenario} | {selected_backend} | {preferred_format} | {recommended_workers} | {fp16_quantize_ms} | {int8_quantize_ms} | {aggregate_mean_ms} |".format(
                **row
            )
        )

    lines.extend(
        [
            "",
            "## Interpretation",
            "",
            "- CPU case provides a baseline for environments without accelerator support.",
            "- GPU/NPU cases validate backend policy selection and expected compression-path behavior.",
            "- Use this alongside Grafana dashboard monitoring/grafana/dashboards/performance/accelerator-backend-compare.json for live telemetry.",
        ]
    )

    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> int:
    parser = argparse.ArgumentParser(description="Build CPU/GPU/NPU benchmark comparison artifacts.")
    parser.add_argument(
        "--output-md",
        default="results/metrics/accelerator_backend_compare.md",
        help="Output markdown report path.",
    )
    parser.add_argument(
        "--output-json",
        default="results/metrics/accelerator_backend_compare.json",
        help="Output json report path.",
    )
    args = parser.parse_args()

    rows = [
        run_backend_case("cpu", "cpu", False),
        run_backend_case("gpu", "cuda", False),
        run_backend_case("npu", "npu", True),
    ]

    md_path = Path(args.output_md)
    json_path = Path(args.output_json)
    write_markdown(md_path, rows)

    payload = {
        "generated_at_unix": int(time.time()),
        "vector_shape": {"clients": 256, "dim": 4096},
        "results": rows,
    }

    json_path.parent.mkdir(parents=True, exist_ok=True)
    json_path.write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8")

    # Keep a deterministic success signal for CI logs.
    score = sum(float(r["aggregate_mean_ms"]) for r in rows)
    if not math.isfinite(score):
        raise SystemExit("non-finite benchmark score produced")
    print(f"wrote {md_path} and {json_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
